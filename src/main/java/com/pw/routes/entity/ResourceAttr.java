package com.pw.routes.entity;

public class ResourceAttr {

    private String resourceMappingKey;
    private String attributeName;
    private String attributeValue;
    private String profileName;
    private String queueName;
    private String concurrency;
    private String jmsHeaderRemoval;
    private String fileName;
    private String location;
    private String fileType;
    private String resourceId;
    private String content;
    private String isDirect;

    public String getResourceMappingKey() {
        return resourceMappingKey;
    }
    public void setResourceMappingKey(String resourceMappingKey) {
        this.resourceMappingKey = resourceMappingKey;
    }
    public String getAttributeName() {
        return attributeName;
    }
    public void setAttributeName(String attributeName) {
        this.attributeName = attributeName;
    }
    public String getAttributeValue() {
        return attributeValue;
    }
    public void setAttributeValue(String attributeValue) {
        this.attributeValue = attributeValue;
    }
    public String getProfileName() {
        return profileName;
    }
    public void setProfileName(String profileName) {
        this.profileName = profileName;
    }
    public String getQueueName() {
        return queueName;
    }
    public void setQueueName(String queueName) {
        this.queueName = queueName;
    }
    public String getConcurrency() {
        return concurrency;
    }
    public void setConcurrency(String concurrency) {
        this.concurrency = concurrency;
    }
    public String getJmsHeaderRemoval() {
        return jmsHeaderRemoval;
    }
    public void setJmsHeaderRemoval(String jmsHeaderRemoval) {
        this.jmsHeaderRemoval = jmsHeaderRemoval;
    }
    public String getFileName() {
        return fileName;
    }
    public void setFileName(String fileName) {
        this.fileName = fileName;
    }
    public String getLocation() {
        return location;
    }
    public void setLocation(String location) {
        this.location = location;
    }
    public String getFileType() {
        return fileType;
    }
    public void setFileType(String fileType) {
        this.fileType = fileType;
    }
    public String getResourceId() {
        return resourceId;
    }
    public void setResourceId(String resourceId) {
        this.resourceId = resourceId;
    }
    public String getContent() {
        return content;
    }
    public void setContent(String content) {
        this.content = content;
    }
    public String getIsDirect() {
        return isDirect;
    }
    public void setIsDirect(String isDirect) {
        this.isDirect = isDirect;
    }


}
